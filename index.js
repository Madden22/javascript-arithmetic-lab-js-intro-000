function add(a,b){
  return a + b
}

function subtract(a,b){
  return a-b
}

function multiply(a,b){
  return a * b
}

function divide(a,b){
  return a / b
}

function increment(n){
  var num = n
  return num++
}

function decrement(n){
  var num = n
  return num--
}

function makeInt(n){
  return parseInt(n, 10)
}

function preserveDecimal(n){
  return parseFloat(n)
}
